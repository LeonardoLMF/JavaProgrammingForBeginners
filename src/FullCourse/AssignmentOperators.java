package FullCourse;

public class AssignmentOperators {
    public static void main(String[] args) {

        int number1 = 10;
        int number2 = 20;

        System.out.println(number1 == number2); // equals to
        System.out.println(number1 != number2); // not equals
        System.out.println(number1 > number2); // greater than
        System.out.println(number1 < number2); // less than
        System.out.println(number1 <= number2); // less or equal
        System.out.println(number1 >= number2); // greater or equal

    }

}
